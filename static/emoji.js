// DOM Elements
const emojiList = document.getElementById('emoji-list');
const emojiCategoryButtons = document.querySelectorAll('.emoji-categories button');

// Declare isEmojiPickerOpen
let isEmojiPickerOpens = false;

// Initialize emoji picker
function initEmojiPicker() {
    // Set default category
    loadEmojiCategory('smileys');
    
    // Add event listeners to category buttons
    emojiCategoryButtons.forEach(button => {
        button.addEventListener('click', () => {
            // Remove active class from all buttons
            emojiCategoryButtons.forEach(btn => btn.classList.remove('active'));
            // Add active class to clicked button
            button.classList.add('active');
            // Load emojis for the selected category
            loadEmojiCategory(button.dataset.category);
        });
    });
}

// Load emojis for a category
function loadEmojiCategory(category) {
    // Clear emoji list
    emojiList.innerHTML = '';
    
    // Add emojis to the list
    emojiData[category].forEach(emoji => {
        const emojiElement = document.createElement('div');
        emojiElement.classList.add('emoji');
        emojiElement.textContent = emoji;
        emojiElement.addEventListener('click', () => {
            insertEmoji(emoji);
        });
        emojiList.appendChild(emojiElement);
    });
}

// Insert emoji into message input
function insertEmoji(emoji) {
    const messageInput = document.getElementById('message-input');
    const cursorPosition = messageInput.selectionStart;
    const textBeforeCursor = messageInput.value.substring(0, cursorPosition);
    const textAfterCursor = messageInput.value.substring(cursorPosition);
    
    messageInput.value = textBeforeCursor + emoji + textAfterCursor;
    
    // Set cursor position after the inserted emoji
    messageInput.selectionStart = cursorPosition + emoji.length;
    messageInput.selectionEnd = cursorPosition + emoji.length;
    
    // Focus on the input
    messageInput.focus();
    
    // Close emoji picker
    document.getElementById('emoji-picker').classList.add('hidden');
    isEmojiPickerOpens = false;
}

// Initialize emoji picker when the DOM is loaded
document.addEventListener('DOMContentLoaded', initEmojiPicker);